void sortowanie_listy(lista **head)
{
	lista *pom = NULL; // obecnie sprawdzany element
	lista *pom_next = NULL; // element nastepny
	lista *pom2 = NULL;
	lista *pom_kontener = NULL; 
	lista *warunek = NULL;

	while((*head)->next != warunek)
	{
		pom = *head;
		pom2 = pom;
		pom_next = pom->next;
		while(pom != warunek)
		{
			if(pom->lisc->ilosc > pom_next->lisc->ilosc)
			{
				pom_kontener = pom_next->next;
				pom_next->next = pom;
				pom->next = pom_kontener;
				if(pom == *head)
				{
					*head = pom_next;
					pom2 = pom_next;
				}
				else
				{
					pom2->next = pom_next;
					pom2 = pom_next;
				}
			}
			else
			{
				pom2 = pom;
				pom = pom->next;
			}
			pom_next = pom->next;
			if(pom_next == warunek)
				warunek = pom;
		}
	}
}
